-- 交通費申請履歴取得
GET_HISTORY=
SELECT
    USERS.KANJI_NAME EXECUTIONER_KANJI_NAME,
    HISTORY.EXECUTION_DATE_TIME,
    HISTORY.TRANS_EXPE_APPLI_ACTION_CD,
    HISTORY.TRANS_EXPE_APPLI_RESULT_CD,
    HISTORY.HISTORY_COMMENT
FROM
    TRANS_EXPE_APPLI_HISTORY HISTORY
INNER JOIN
    USERS
ON
    HISTORY.EXECUTIONER_ID = USERS.USER_ID
WHERE
    HISTORY.TRANS_EXPE_APPLI_ID = ?
ORDER BY
    HISTORY.EXECUTION_DATE_TIME