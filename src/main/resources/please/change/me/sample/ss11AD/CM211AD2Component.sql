-- 交通費申請一覧検索
SELECT_APPLICATION_BY_CONDITION=
SELECT
    TRANS_EXPE_APPLI_ID APPLICATION_ID,
    WF_INSTANCE_ID,
    TRANS_EXPE_APPLI_STATUS_CD,
    TRANS_DEVICE_CD,
    DEPARTURE,
    DESTINATION,
    TRANS_EXPENSE,
    INSERT_DATE_TIME
FROM
    TRANS_EXPE_APPLICATION
WHERE
    INSERT_USER_ID = :loginUserId
    AND $if(statusCd) {TRANS_EXPE_APPLI_STATUS_CD = :statusCd}
    AND $if(deviceCd) {TRANS_DEVICE_CD = :deviceCd}
ORDER BY
    INSERT_DATE_TIME DESC